# 1. strip() : Removes leading and trailing whitespace from a string, including spaces, tabs, newlines, etc.
# 2. lstrip() : Removes leading whitespace from a string. : at the beggining of the string
# 3. rstrip() : Removes trailing whitespace from a string. : at the end of the string
# 4. split() : Splits a string into a list of substrings based on a delimiter. : delimiter is space by default
# 5. join() : Concatenates a list of strings into a single string.
# 6. replace() : Replaces all occurrences of a substring within a string.
