# Functions : A function is a block of code which only runs when it is called. You can pass data, known as parameters, into a function.
# A function can return data as a result.
# Arguments : Information can be passed into functions as arguments.
# Parameters or Arguments? The terms parameter and argument can be used for the same thing: information that are passed into a function.

def calc_sum(a, b):
    return a + b 


print(calc_sum(10, 20))
print(calc_sum(100, 200))





# Now 10, 20, 100, 200 are arguments and a, b are parameters.
